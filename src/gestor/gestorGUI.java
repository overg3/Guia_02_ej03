    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        DefaultTableModel productTableModel
                = (DefaultTableModel) productTable.getModel(); //Get generated table model

        if (productField.getText().isEmpty() || priceField.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese el nombre y precio del "
                    + "producto", "ERROR", JOptionPane.INFORMATION_MESSAGE);
        } else {
            String product = productField.getText();
            String category = (String) categoryBox.getSelectedItem(); //JComboBox guarda los elementos como Objetos, por lo tanto debe ser casteado a String

            try {
                float price = Float.parseFloat(priceField.getText());
                productTableModel.addRow(new Object[]{product, category,
                    "$ " + price});
                clearFields();
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "El precio ingresado es "
                        + "incorrecto", "ERROR", JOptionPane.INFORMATION_MESSAGE);
                priceField.setText("");
            }
        }


    }//GEN-LAST:event_addButtonActionPerformed

