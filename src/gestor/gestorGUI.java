    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        DefaultTableModel productTableModel
                = (DefaultTableModel) productTable.getModel(); //Get generated table model
        
        int rowSelected = productTable.getSelectedRow();
        
        if (rowSelected != -1) {
            productTableModel.removeRow(rowSelected);
        } else {
            JOptionPane.showMessageDialog(this, "No ha se leccionado ningun "
                    + "producto", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_deleteButtonActionPerformed

